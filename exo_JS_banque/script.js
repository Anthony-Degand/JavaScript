// Menu

const menu = (retrait)

switch (menu) {
    case retrait:
        console.log("Retrait");
        break;                // Break sert a passer le switch et passer a la futur condition suivante
    case solde:
        console.log("Solde");
        break;
    case virement:
        console.log("Virement");
        break;
    case dépot:
        console.log("Dépot");
        break;
    case historique:
        console.log("Historique");
        break;
    case rib:
        console.log("Rib");
        break;
    case retour:
        console.log("Retour");
        break;

   
        default:                    // toujours mettre le default a la fin du switch
        alert("erreur");
        break;
   
}














